#include "headers.hpp"

class Solution {
public:
  uint32_t reverseBits(uint32_t n) {
    // 1010,1010,1010,1010,1010,1010,1010,1010 and
    // 0101,0101,0101,0101,0101,0101,0101,0101
    n = ((n & 0xaaaaaaaa) >> 1) | ((n & 0x55555555) << 1);
    // 1100,1100,1100,1100,1100,1100,1100,1100 and
    // 0011,0011,0011,0011,0011,0011,0011,0011
    n = ((n & 0xcccccccc) >> 2) | ((n & 0x33333333) << 2);
    // 1111,0000,1111,0000,1111,0000,1111,0000 and
    // 0000,1111,0000,1111,0000,1111,0000,1111
    n = ((n & 0xf0f0f0f0) >> 4) | ((n & 0x0f0f0f0f) << 4);
    // 11111111,00000000,11111111,00000000 and
    // 00000000,11111111,00000000,11111111
    n = ((n & 0xff00ff00) >> 8) | ((n & 0x00ff00ff) << 8);
    // 1111111111111111,0000000000000000 and
    // 0000000000000000,1111111111111111
    n = (n >> 16) | (n << 16);
    return n;
  }
};